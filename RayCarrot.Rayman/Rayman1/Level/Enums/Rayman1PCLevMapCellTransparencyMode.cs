namespace RayCarrot.Rayman.Rayman1
{
    /// <summary>
    /// The transparency mode for a <see cref="Rayman1PCLevMapCell"/>
    /// </summary>
    public enum Rayman1PCLevMapCellTransparencyMode
    {
        /// <summary>
        /// Indicates that the cell has no transparency
        /// </summary>
        NoTransparency = 0,

        /// <summary>
        /// Indicates that the cell is fully transparent
        /// </summary>
        FullyTransparent = 1,

        /// <summary>
        /// Indicates that the cell is partially transparent
        /// </summary>
        PartiallyTransparent = 2
    }
}