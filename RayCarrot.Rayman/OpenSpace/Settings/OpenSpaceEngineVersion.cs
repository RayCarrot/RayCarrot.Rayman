namespace RayCarrot.Rayman.OpenSpace
{
    // NOTE: The enum member values are used to indicate which OpenSpaceGame belongs to which engine version

    /// <summary>
    /// The OpenSpace engine versions
    /// </summary>
    public enum OpenSpaceEngineVersion
    {
        /// <summary>
        /// The version used for Tonic Trouble
        /// </summary>
        TonicTrouble = 1,

        /// <summary>
        /// The version used for the Ubisoft Montreal games
        /// </summary>
        Montreal = 4,

        /// <summary>
        /// The version used for Rayman 2
        /// </summary>
        Rayman2 = 10,

        /// <summary>
        /// The version used for Rayman 3
        /// </summary>
        Rayman3 = 16
    }
}