namespace RayCarrot.Rayman
{
    /// <summary>
    /// The available byte orders
    /// </summary>
    public enum ByteOrder
    {
        /// <summary>
        /// The default byte order on the current machine
        /// </summary>
        Default,

        /// <summary>
        /// Little endian byte order
        /// </summary>
        LittleEndian,

        /// <summary>
        /// Big endian byte order
        /// </summary>
        BigEndian
    }
}